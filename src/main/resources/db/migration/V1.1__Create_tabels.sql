
 create TABLE files(
    id  /*SERIAL PRIMARY KEY*/ BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50),
    filePath VARCHAR(150)
       );


create TABLE users(
    id /*SERIAL PRIMARY KEY*/ BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
    name VARCHAR(50)
    );


create TABLE events(
id /*SERIAL PRIMARY KEY*/ BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
user_id BIGINT,
file_id BIGINT,
   FOREIGN KEY (user_id)  REFERENCES users (id) ON DELETE CASCADE,
   FOREIGN KEY (file_id)  REFERENCES files (id) ON DELETE CASCADE
   );


